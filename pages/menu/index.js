import React, { useState } from "react";
import { useSelector } from "react-redux";
import Header from "../../components/GeneralUI/Header";
import Head from "next/head";
import { motion } from "framer-motion";
import BottomContents from "../../components/MenuPage/BottomContents";
import FilterOption from "../../components/MenuPage/FIlterOption";
import SwiperInstantiation from "../../components/MenuPage/SwiperInstantiation";

const mon_com = [
  {
    img: "https://swiperjs.com/demos/images/nature-1.jpg",
    name: "Mỳ ý phô mai chay",
  },
  {
    img: "https://swiperjs.com/demos/images/nature-2.jpg",
    name: "Mỳ ý phô mai chay ngon",
  },
  {
    img: "https://swiperjs.com/demos/images/nature-3.jpg",
    name: "Cơm chay",
  },
  {
    img: "https://swiperjs.com/demos/images/nature-4.jpg",
    name: "Cơm chay ngon",
  },
];

const mon_soi = [
  {
    img: "https://swiperjs.com/demos/images/nature-1.jpg",
    name: "rau chay",
  },
  {
    img: "https://swiperjs.com/demos/images/nature-2.jpg",
    name: "nấm",
  },
  {
    img: "https://swiperjs.com/demos/images/nature-3.jpg",
    name: "Cơm chay",
  },
];

const filter_options = [
  "Món cơm",
  "Món sợi",
  "Món canh",
  "Món rau",
  "Món ăn kèm",
];

const Menu = () => {
  const [dishName, setDishName] = useState(mon_com[0].name);
  const [openFilterModal, setOpenFilterModal] = useState(false);
  const [selectedFilterOption, setSelectedFilterOption] = useState(0);
  const [numberOfDish, setNumberOfDish] = useState(mon_com.length);

  const pageWillBeExitingFrom = useSelector(
    (state) => state.uiReducer.pageExitingFrom
  );

  // select option function
  const selectOption = (value) => {
    setSelectedFilterOption(value);
    // Set the number of dishes of each dish category
    if (value === 0) {
      setNumberOfDish(mon_com.length);
    } else if (value === 1) {
      setNumberOfDish(mon_soi.length);
    }
  };

  return (
    <div>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
      </Head>

      <Header />

      {pageWillBeExitingFrom == "menu" && (
        <motion.main
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{ duration: 0.8 }}
        >
          {selectedFilterOption == 0 && (
            <SwiperInstantiation array={mon_com} setDishName={setDishName} />
          )}

          {selectedFilterOption == 1 && (
            <SwiperInstantiation array={mon_soi} setDishName={setDishName} />
          )}

          {/* Dish name */}
          <h1 className="absolute bottom-[35%] top-[65%] right-0 left-0 z-20 text-center w-2/3 mx-auto text-2xl font-medium">
            {dishName}
          </h1>

          {/* Bottom contents consitsting of filter button and dish category heading */}
          <BottomContents
            setOpenFilterModal={setOpenFilterModal}
            filter_options={filter_options}
            selectedFilterOption={selectedFilterOption}
            numberOfDish={numberOfDish}
          />

          {/* Filter Modal */}
          {openFilterModal && (
            <>
              {/* Modal div */}
              <div
                onClick={() => setOpenFilterModal(false)}
                className="absolute z-[60] w-full top-0 bottom-0 bg-black bg-opacity-70"
              ></div>
              {/* Bottom Content div */}
              <div className="w-full h-[170px] bg-white rounded-t-lg absolute z-[60] bottom-0 p-6">
                {filter_options.map((name, index) => (
                  <FilterOption
                    key={name}
                    name={name}
                    selectFilterOption={selectOption}
                    index={index}
                    isSelected={selectedFilterOption == index}
                  />
                ))}
                <h1
                  onClick={() => {
                    setOpenFilterModal(false);
                  }}
                  className="cursor-pointer absolute bottom-5 right-7 font-medium mt-[55px]"
                >
                  Đóng
                </h1>
              </div>
            </>
          )}
        </motion.main>
      )}
    </div>
  );
};

export default Menu;
